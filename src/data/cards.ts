const COURT_TITLES = ["Паж", "Рыцарь", "Королева", "Король"];
const COURT_KEYS = ["page", "knight", "queen", "king"];

function buildMinorArcana(): CardDef[] {
  const suits: Suit[] = ["wands", "cups", "swords", "pentacles"];
  const cards: CardDef[] = [];

  for (const suit of suits) {
    const numbered = NUMBERED(suit, SUIT_NUMBERED[suit]);
    for (const rank of numbered) {
      const rankLabel = rank.key === "ace" ? "Туз" : String(rank.number);
      cards.push({
        slug: `${suit}-${rank.key}`,
        name: `${rankLabel} ${SUIT_LABEL[suit]}`,
        arcana: "minor",
        suit,
        number: rank.number,
        image: `/cards/${suit}-${rank.key}.jpg`,
        upright: rank.upright,
        reversed: rank.reversed,
      });
    }

    SUIT_COURT[suit].forEach(([upright, reversed], i) => {
      cards.push({
        slug: `${suit}-${COURT_KEYS[i]}`,
        name: `${COURT_TITLES[i]} ${SUIT_LABEL[suit]}`,
        arcana: "minor",
        suit,
        image: `/cards/${suit}-${COURT_KEYS[i]}.jpg`,
        upright,
        reversed,
      });
    });
  }

  return cards;
}

export const CARDS: CardDef[] = [...MAJOR_ARCANA, ...buildMinorArcana()];

export function getCardBySlug(slug: string): CardDef | undefined {
  return CARDS.find((c) => c.slug === slug);
}
