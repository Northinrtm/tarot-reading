interface MinorRank {
  key: string;
  number?: number;
  upright: string;
  reversed: string;
}

const NUMBERED = (suit: Suit, entries: [string, string][]): MinorRank[] =>
  entries.map(([upright, reversed], i) => ({
    key: i === 0 ? "ace" : String(i + 1).padStart(2, "0"),
    number: i + 1,
    upright,
    reversed,
  }));

